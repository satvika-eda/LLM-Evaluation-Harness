"""
Shared types for the scorers package.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass
class ScoringInput:
    """
    Flat record passed to every scorer for one model response.

    Constructed by the orchestrator by joining Response rows with their
    parent Question row so scorers never need to touch the DB themselves.

    Fields
    ------
    response_id   : PK of the responses table row  (FK target for scores)
    response_text : text generated by the model
    question      : original question text
    ground_truth  : reference / expected answer
    context       : concatenated supporting passages (empty string for
                    TruthfulQA — scorers that need context must check this)
    model_name    : canonical model identifier, e.g. "gpt-4o"
    dataset_name  : "truthfulqa" | "hotpotqa" (used to gate context metrics)
    """

    response_id: int
    response_text: str
    question: str
    ground_truth: str
    context: str
    model_name: str
    dataset_name: str
